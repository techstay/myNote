package basics

//布尔类型
var b bool

//字符串
var str string

//整数类型
var i8 int8
var i16 int16
var i32 int32
var i64 int64

//无符号整数
var ui8 uint8
var ui16 uint16
var ui32 uint32
var ui64 uint64

//int比较特殊，在32位系统下有32位，在64位系统下有64位
var i int
var ui uint

//指针类型，和int情况类似
var ptr uintptr

//byte，是int8的别名
var bb byte

//rune，是int32的别名，代表Unicode代码点
var cp rune

//浮点数类型
var float float32
var double float64

//复数类型
var com64 complex64
var com128 complex128

