package tech.techstay.basic

fun main() {
    // integers
    val byte: Byte = 1
    val short: Short = 2
    val int: Int = 3
    val long: Long = 4000L

    // kotlin doesn't support octal
    val decimal = 100
    val binary = 0b10001100
    val hexadecimal = 0xcafe

    // float numbers
    val float: Float = 3.14f
    val double: Double = 3.14

    // boolean
    val booleanValue: Boolean = true || false

    // characters
    val char: Char = 'a'

    // strings
    val str: String = "abc"

    // arrays
    val array: Array<Int> = arrayOf(1, 2, 3, 4, 5)
    val intArray: IntArray = intArrayOf(1, 2, 3, 4)
}
