package tech.techstay.basic

fun main() {
    // signed left shift
    println(1 shl 2)
    // signed right shift
    println(1 shr 2)
    // unsigned right shit
    println(1 ushr 2)

    // bitwise and or not xor
    println(1 and 2)
    println(2 or 1)
    println(1 xor 2)
    println(1.inv())
}
