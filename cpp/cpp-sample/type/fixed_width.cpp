#include <cstdint>
#include <iostream>

using namespace std;

int main()
{
    // fix-width integers, available since c++ 11
    int8_t i8     = 100;
    int16_t i16   = 100;
    int32_t i32   = 10'000;
    int64_t i64   = 10'000'000;
    uint8_t ui8   = 200;
    uint16_t ui16 = 2'000;
    uint32_t ui32 = 20'000;
    uint64_t ui64 = 20'000'000;
}